#!/bin/bash

# inspired by init scripts of CDH4

# Starts a Hadoop nodemanager
#
# chkconfig: 345 85 15
# description: Hadoop nodemanager
#
### BEGIN INIT INFO
# Provides:          hdp-nodemanager
# Short-Description: Hadoop nodemanager
# Default-Start:     3 4 5
# Default-Stop:      0 1 2 6
# Required-Start:    $syslog $remote_fs
# Required-Stop:     $syslog $remote_fs
# Should-Start:
# Should-Stop:
### END INIT INFO

### yarn-daemon.sh
#   YARN_CONF_DIR  Alternate conf dir. Default is ${YARN_HOME}/conf.
#   YARN_LOG_DIR   Where log files are stored.  PWD by default.
#   YARN_MASTER    host:path where hadoop code should be rsync'd from
#   YARN_PID_DIR   The pid files are stored. /tmp by default.
#   YARN_IDENT_STRING   A string representing this instance of hadoop. $USER by default
#   YARN_NICENESS The scheduling priority for daemons. Defaults to 0.

. /lib/lsb/init-functions
. /etc/default/hadoop

if [ -f /etc/default/hadoop-custom ] ; then
  . /etc/default/hadoop-custom
fi
YARN_CONF_DIR=$HADOOP_CONF_DIR
YARN_LOG_DIR=$YARN_LOG_DIR
YARN_PID_DIR=$YARN_PID_DIR
YARN_IDENT_STRING=$YARN_USER

YARN_COMMAND="nodemanager"

##

RETVAL_SUCCESS=0

STATUS_RUNNING=0
STATUS_DEAD=1
STATUS_DEAD_AND_LOCK=2
STATUS_NOT_RUNNING=3
STATUS_OTHER_ERROR=102

ERROR_PROGRAM_NOT_INSTALLED=5
ERROR_PROGRAM_NOT_CONFIGURED=6

RETVAL=0
SLEEP_TIME=5
PROC_NAME="java"

YARN_COMMAND_LONG_NAME="yarn-"$YARN_IDENT_STRING"-"$YARN_COMMAND

DAEMON="hdp-"$YARN_COMMAND
DESC="Hadoop "$YARN_COMMAND
EXEC_PATH="/usr/lib/hadoop-yarn/sbin/yarn-daemon.sh"
SVC_USER=$YARN_IDENT_STRING
DAEMON_FLAGS=$YARN_COMMAND
CONF_DIR=$YARN_CONF_DIR
PIDFILE=$YARN_PID_DIR"/"$YARN_COMMAND_LONG_NAME".pid"
LOCKDIR="/var/lock/subsys"
LOCKFILE="$LOCKDIR/"$YARN_COMMAND_LONG_NAME
WORKING_DIR="/var/lib/hadoop-yarn"

install -d -m 0755 -o $YARN_USER -g $HADOOP_GROUP "$YARN_PID_DIR" 1>/dev/null 2>&1 || :
[ -d "$LOCKDIR" ] || install -d -m 0755 $LOCKDIR 1>/dev/null 2>&1 || :

start() {
  [ -x $EXEC_PATH ] || exit $ERROR_PROGRAM_NOT_INSTALLED
  [ -d $CONF_DIR ] || exit $ERROR_PROGRAM_NOT_CONFIGURED
  log_success_msg "Starting ${DESC}: "

  su -s /bin/bash $SVC_USER -c "cd $WORKING_DIR && $EXEC_PATH --config '$CONF_DIR' start $DAEMON_FLAGS"
  # PIDFILE will be generated by hadoop-daemon.sh

  # Some processes are slow to start
  sleep $SLEEP_TIME
  checkstatusofproc
  RETVAL=$?

  [ $RETVAL -eq $RETVAL_SUCCESS ] && touch $LOCKFILE
  return $RETVAL
}

stop() {
  log_success_msg "Stopping ${DESC}: "
  su -s /bin/bash $SVC_USER -c "$EXEC_PATH --config '$CONF_DIR' stop $DAEMON_FLAGS"
  RETVAL=$?

  [ $RETVAL -eq $RETVAL_SUCCESS ] && rm -f $LOCKFILE $PIDFILE
}

restart() {
  stop
  start
}

checkstatusofproc(){
  pidofproc -p $PIDFILE $PROC_NAME > /dev/null
}

checkstatus(){
  checkstatusofproc
  status=$?

  case "$status" in
    $STATUS_RUNNING)
      log_success_msg "${DESC} is running"
      ;;
    $STATUS_DEAD)
      log_failure_msg "${DESC} is dead and pid file exists"
      ;;
    $STATUS_DEAD_AND_LOCK)
      log_failure_msg "${DESC} is dead and lock file exists"
      ;;
    $STATUS_NOT_RUNNING)
      log_failure_msg "${DESC} is not running"
      ;;
    *)
      log_failure_msg "${DESC} status is unknown"
      ;;
  esac
  return $status
}

condrestart(){
  [ -e $LOCKFILE ] && restart || :
}

check_for_root() {
  if [ $(id -ur) -ne 0 ]; then
    echo 'Error: root user required'
    echo
    exit 1
  fi
}

service() {
  case "$1" in
    start)
      check_for_root
      start
      ;;
    stop)
      check_for_root
      stop
      ;;
    status)
      checkstatus
      RETVAL=$?
      ;;
    restart)
      check_for_root
      restart
      ;;
    condrestart|try-restart)
      check_for_root
      condrestart
      ;;
    *)
      echo $"Usage: $0 {start|stop|status|restart|try-restart|condrestart}"
      exit 1
  esac
}

service "$1"

exit $RETVAL
